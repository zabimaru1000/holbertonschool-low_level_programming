#include "holberton.h"

int add(int, int);

/**
 * add - adds the sum of integers a & b and stores it in c.
 *
 * @a: 1st parameter that takes an integer to be added to b.
 * @b: 2nd parameter that takes another integer to be added to a/
 *
 * Return: int c (returns sum of a & b to main)
 */
int add(int a, int b)
{
	int c;

	c = a + b;

	return (c);
}
